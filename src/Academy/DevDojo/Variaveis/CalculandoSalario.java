package Academy.DevDojo.Variaveis;

public class CalculandoSalario {
    public static void main(String[] args){
       float wage = 2500.00f;
       float result = wage * 0.30f;
       System.out.println("30% of the wage is= " + result);
       result = wage * 0.15f;
       System.out.println("15% of the wage is= " + result);
       result = wage * 0.05f;
       System.out.println("5% of the wage is= " + result);
       System.out.println("Esses foram os calculos :D");
    }
}